// Create the MathOp class to house the calculations of add and subtract
class MathOp {

// compute add and subtract methods
  double MathAdd(double firstNum, double secondNum) {
    double add = firstNum + secondNum;
    return add;
  } // end of MathAdd method
  double MathSub(double firstNum, double secondNum) {
    double sub = firstNum - secondNum;
    return sub;
  }// end of MathSub method
} // end of MathOp class
